function displaytopSlider(topSlider) {
//   console.log(topSlider);
  document.querySelector("#topSlider").innerText="";
  topSlider.map((elem) => {
    let outerDiv = document.createElement("div");
    let images = document.createElement("img");
    images.src = elem.image;
    outerDiv.append(images);
    document.querySelector("#topSlider").append(outerDiv);
  });
}


function dislayTopDivData(topDivData){
    // console.log(topDivData);
    document.querySelector("#topDivData").innerText = "";
    topDivData.map((elem)=>{
        let mainDiv = document.createElement("div");
        let images = document.createElement("img");
        images.src=elem.image;
        let description = document.createElement("h4");
        description.innerText = elem.desc;
        
        mainDiv.append(images, description);
        document.querySelector("#topDivData").append(mainDiv);
    })
}


